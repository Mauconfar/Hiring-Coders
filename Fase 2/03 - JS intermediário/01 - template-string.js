let meuNome = "Maucon";
let meuSobrenome = "Rodrigues";
let minhaProfissao = "Secretário Parlamentar";

// Sem template String.
console.log("Meu nome é " + meuNome + " " + meuSobrenome + ". Minha profissão é: " + minhaProfissao + ".");


// Com template sting.
console.log(`Meu nome é ${meuNome} ${meuSobrenome}. Minha profissão é: ${minhaProfissao}.`)

//Serve também para fazer operações:
console.log(`O resultado da soma de 1 + 1 é ${1+1}.`)
